public class Treasure {
    int x ;
    int y;
    char a;

    Treasure(int x, int y,char a){
        this.x=x;
        this.y=y;
        this.a=a;
    }
    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }

    public int getX() {
        return x;
    }
    public void setX(int x) {
        this.x = x;
    }
}
